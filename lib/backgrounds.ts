export const isGradient = (id: BgId) => BACKGROUNDS[id].url.startsWith('gradient:');

export const getBgUrl = (id: BgId, width = 1600) => {
  const bg = BACKGROUNDS[id];
  if (!bg?.url || isGradient(id)) return '';
  return `${bg.url}?w=${width}&q=85&auto=format&fit=crop&fm=webp`;
};

export const getBgSrcSet = (id: BgId) => {
  const bg = BACKGROUNDS[id];
  if (!bg?.url || isGradient(id)) return '';
  return `${bg.url}?w=1200&fm=webp 1x, ${bg.url}?w=2400&fm=webp 2x, ${bg.url}?w=3200&fm=webp 3x`;
};